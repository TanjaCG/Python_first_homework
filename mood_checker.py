mood = input("What is your mood today?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Go get an ice cream.")
elif mood == "excited":
    print("Bring it on!")
elif mood == ("relaxed"):
    print("Just chill..")
else:
    print("I don't recognize this mood")

